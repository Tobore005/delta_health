import { Head, useForm } from '@inertiajs/react';
import { LoaderCircle } from 'lucide-react';
import validator from 'validator'; // Make sure this is installed: npm i validator

import InputError from '@/components/input-error';
import TextLink from '@/components/text-link';
import { Button } from '@/components/ui/button';
import { Input } from '@/components/ui/input';
import { Label } from '@/components/ui/label';
import AuthLayout from '@/layouts/auth-layout';
import { login } from '@/routes';

export default function ClinicForgotPassword({ status }: { status?: string }) {
    const { data, setData, post, processing, errors, setError } = useForm({
        email: '',
    });

    const handleSubmit = (e: React.FormEvent) => {
        e.preventDefault();

        if (!validator.isEmail(data.email)) {
            setError('email', 'Please enter a valid clinic email address.');
            return;
        }

        post('/clinic-forgot-password', {
            preserveScroll: true,
        });
    };

    return (
        <AuthLayout
            title="Forgot password"
            description="Enter your email to receive a password reset link"
        >
            <Head title="Forgot password" />

            {status && (
                <div className="mb-4 text-center text-sm font-medium text-green-600">
                    {status}
                </div>
            )}

            <form onSubmit={handleSubmit} className="space-y-6">
                <div className="grid gap-2">
                    <Label htmlFor="email">Email address</Label>
                    <Input
                        id="email"
                        type="email"
                        name="email"
                        value={data.email}
                        onChange={(e) => setData('email', e.target.value)}
                        autoComplete="off"
                        autoFocus
                        placeholder="clinic@example.com"
                    />
                    <InputError message={errors.email} />
                </div>

                <div className="my-6 flex items-center justify-start">
                    <Button className="w-full" disabled={processing}>
                        {processing && <LoaderCircle className="h-4 w-4 animate-spin" />}
                        Email password reset link
                    </Button>
                </div>
            </form>

            <div className="space-x-1 text-center text-sm text-muted-foreground">
                <span>Or, return to</span>
                <TextLink href={login()}>log in</TextLink>
            </div>
        </AuthLayout>
    );
}